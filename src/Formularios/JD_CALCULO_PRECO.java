    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        float compra = Float.parseFloat(txtValorCompra.getText().replace(".", "").replaceAll(",", "."));
        float frete = Float.parseFloat(txtFrete.getText().replace(".", "").replaceAll(",", "."));
        float embalagem = Float.parseFloat(txtEmbalagem.getText().replace(".", "").replaceAll(",", "."));
        float comissao = Float.parseFloat(txtComissao.getText().replace(".", "").replaceAll(",", "."));
        float taxa = Float.parseFloat(txtTaxas.getText().replace(".", "").replaceAll(",", "."));

        float custoTotal = compra + frete + embalagem;
        float margem = Float.parseFloat(txtMargem.getText().replace(".", "").replaceAll(",", "."));
        float acrescimos = (comissao + taxa);

        float precoVenda = (custoTotal / (100 - (margem + acrescimos))) * 100;
        float lucro = precoVenda - custoTotal;
        float valorComissao = comissao / 100 * precoVenda;
        float valorTaxa = taxa / 100 * precoVenda;

        DecimalFormat df = new DecimalFormat("#,##0.00");

        String valorVendaFormant = df.format(precoVenda);
        String lucroFormant = df.format(lucro);
        String custoFormat = df.format(custoTotal);
        String comissaoFormat = df.format(valorComissao);
        String taxaFormat = df.format(valorTaxa);

        txtPrecoVenda.setText(valorVendaFormant);
        txtLucroBruto.setText(lucroFormant);
        txtCustoTotal.setText(custoFormat);
        lblValorComissao.setText("Valor da comissão:   " + "R$ " + comissaoFormat + " ");
        lblValorTaxas.setText("Valor da taxa do cartão:  " + "R$ " + taxaFormat + " ");

    }//GEN-LAST:event_btnCalcularActionPerformed

