    public void buscaUltimaCompra(String codigo) {

        Conexao conn = new Conexao();
        try {
          //  JOptionPane.showMessageDialog(null, codigo);
            PreparedStatement pst = null;
            ResultSet rs;
            pst = conn.getConexao().prepareStatement("select preco from compra where codProduto =?");
            pst.setString(1, codigo);
            rs = pst.executeQuery();
            
            while (rs.next()) {
                txtValorCompra.setText(rs.getString("preco"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar ultima preço de compra");
        }

    }

