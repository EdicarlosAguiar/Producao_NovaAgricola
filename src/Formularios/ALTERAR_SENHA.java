    public void buscaUsuario() {

        try {
            Conexao conn = new Conexao();

            try {
                PreparedStatement pst = null;
                ResultSet rs;
                pst = conn.getConexao().prepareStatement("select PASS from usuario where USER =?");
                pst.setString(1, txtUsuario.getText().replace(" ", ""));
                rs = pst.executeQuery();

                while (rs.next()) {
                    String senhax = (rs.getString(1));
                    senha = senhax;

                }
                conn.getConexao().close();

            } catch (Exception e) {

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao Conectar com banco!" + ex);
        }

    }

    public void alterar() {

        try {

            int resp = JOptionPane.showConfirmDialog(null, "Confirma a alteração da senha" + "?", "Alteração de senha", JOptionPane.YES_NO_OPTION);
            if (resp == 0) {

                Conexao conn = new Conexao();

                PreparedStatement pst = conn.getConexao().prepareStatement("UPDATE usuario SET PASS=? where USER =?");

                pst.setString(1, txtNovaSenha.getText());
                pst.setString(2, txtUsuario.getText().replace(" ",""));

                pst.executeUpdate();

                Confirmacao conf = new Confirmacao(new javax.swing.JFrame(), true);
                conf.setVisible(true);

                pst.close();
                this.dispose();
            } else {
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar alterar o produto! " + e, "Alteração de Produto", JOptionPane.ERROR_MESSAGE);
        }

    }

