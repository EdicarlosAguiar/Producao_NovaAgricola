    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        String novaSenhaInformada = txtSenhaAtual.getText();

        if (txtNovaSenha.getText().equals(txtConfirmaNovaSenha.getText())) {

            if (novaSenhaInformada.equals(senha)) {
                alterar();
                this.dispose();

            } else {
                senhaInvalida erro = new senhaInvalida(new javax.swing.JFrame(), true);
                erro.setVisible(true);
                 //  JOptionPane.showMessageDialog(null, senha);
                txtSenhaAtual.requestFocus();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Há uma divergência entre a nova senha e a confirmação!");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

