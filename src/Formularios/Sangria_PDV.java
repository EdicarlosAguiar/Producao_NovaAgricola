    public void buscaAprovador() {

        lblAprovador.setText("");

        Conexao conn = new Conexao();

        try {
            PreparedStatement pst = null;
            ResultSet rs;
            pst = conn.getConexao().prepareStatement("select aprovador from aprovadores_mov_caixa where senha = ?");
            pst.setString(1, txtSenha.getText());
            rs = pst.executeQuery();

            while (rs.next()) {
                aprovador = rs.getString(1);
                lblAprovador.setText("Aprovador válido");
                lblAprovador.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Imagens/IconConf.png"))); // NOI18N

            }
            conn.getConexao().close();

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(this, "Erro ao Conectar com banco!" + ex);

        }
        if (lblAprovador.getText().equals("")) {
            lblAprovador.setText("Aprovador inválido");
            lblAprovador.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Imagens/fechar.png"))); // NOI18N

        } else {

        }
    }

