    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        double saldo = Double.parseDouble(lblSaldo.getText().replace("R$ ", "").replace(".", "").replaceAll(",", "."));
        double sangria = Double.parseDouble(txtValor.getText().replace("R$ ", "").replace(".", "").replaceAll(",", "."));

        if (saldo < sangria) {
            JOptionPane.showMessageDialog(this, "Não insuficiente", "AGUI TECH", 1);
        } else {
            ControlCaixaPDV control = new ControlCaixaPDV();
            if (lblAprovador.getText().equals("Aprovador inválido")) {

                ANP_APVD01 anp = new ANP_APVD01(new javax.swing.JFrame(), true);
                anp.setVisible(true);

            } else {
                DateTimeFormatter dataBase = DateTimeFormatter.ofPattern("dd/MM/yyyy " + "|" + " HH:mm");
                String dataAtual = dataBase.format(LocalDateTime.now());

                ModelOperacaoCaixaPDV modCaixa = new ModelOperacaoCaixaPDV();

                modCaixa.setSaida(Double.parseDouble(txtValor.getText().replace("R$ ", "").replace(".", "").replaceAll(",", ".")));
                modCaixa.setCodigo(Integer.parseInt(lblCodCaixa.getText()));
                modCaixa.setOperador(lblOperador.getText());
                modCaixa.setOperacao("SANGRIA/RETIRADA");
                modCaixa.setStatus("ABERTO");
                modCaixa.setAprovador(aprovador);
                modCaixa.setData(dataAtual);
                modCaixa.setConta((String)cbContas.getSelectedItem());

                control.sangriaRetirada(modCaixa);

                dispose();
            }
        }

    }//GEN-LAST:event_btnConfirmarActionPerformed

