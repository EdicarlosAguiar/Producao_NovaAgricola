    public void calculo() {

        double devido = Double.parseDouble(txtDevido.getText().replace("R$ ", "").replace(".", "").replaceAll(",", "."));
        double pago = Double.parseDouble(txtPago.getText().replace("R$ ", "").replace(".", "").replaceAll(",", "."));
        double troco = pago - devido;

        DecimalFormat df = new DecimalFormat("#,##0.00");
        String valorFormatado = df.format(troco);

        txtTroco.setText("R$   " + valorFormatado);

    }

